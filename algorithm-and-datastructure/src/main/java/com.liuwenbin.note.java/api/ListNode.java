package com.liuwenbin.note.java.api;

/**
 * @ Description：链表节点的数据结构
 * @ Author     ：liuwenbin
 * @ Date       ：2018/12/11
 */

public class ListNode {

    /**
     * 链表节点存储的值
     */
    public int val;

    /**
     * 链表下一个结点
     */
    public ListNode next;

    public ListNode(int x) {
        val = x;
    }
}
